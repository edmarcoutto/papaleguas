var dia = "Quinta"

switch (dia) {
        case "Segunda-Feira":
        console.log("Dia de começar a semana");
        break;

        case "Terça-Feira":
        console.log("Hora de aprender coisas novas");
        break;

        case "Quarta-Feira":
        console.log("Metade da Semana, continue firme");
        break;

        case "Quinta-Feira":
        console.log("Quase lá, mais um pouco de esforço");
        break;

        case "Sexta-Feira":
        console.log("Dia de comemorar, o fim de semana chegou");
        break;

        default:
        console.log("Final de Semana");
        break;
}